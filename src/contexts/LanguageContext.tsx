
import React, { createContext, useContext, useState, useEffect } from 'react';

// Define the structure of our translations
interface Translations {
  [key: string]: string;
}

// Define the structure of our language context
interface LanguageContextProps {
  language: string;
  setLanguage: (language: string) => void;
  t: (key: string) => string;
}

// Create the language context with a default value
const LanguageContext = createContext<LanguageContextProps>({
  language: 'en',
  setLanguage: () => {},
  t: (key: string) => key, // Default translation returns the key itself
});

// Language context provider component
export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguage] = useState('en');

  // Load language from localStorage on component mount
  useEffect(() => {
    const storedLanguage = localStorage.getItem('language');
    if (storedLanguage) {
      setLanguage(storedLanguage);
    }
  }, []);

  // Save language to localStorage whenever it changes
  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  // Translation function
  const t = (key: string): string => {
    try {
      // Attempt to return the translation, if it doesn't exist, return the key
      return translations[language][key] || key;
    } catch (error) {
      console.warn(`Translation key "${key}" not found in language "${language}".`);
      return key;
    }
  };

  // Provide the context value
  const contextValue: LanguageContextProps = {
    language,
    setLanguage,
    t,
  };

  return (
    <LanguageContext.Provider value={contextValue}>
      {children}
    </LanguageContext.Provider>
  );
};

// Custom hook to use the language context
export const useLanguage = (): LanguageContextProps => {
  return useContext(LanguageContext);
};

